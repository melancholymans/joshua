//TESTのためのヘルパー関数
bool array_check(Move anser, MoveStack *m)
{
	for (int i=0; i < 512; i++){
		if (m++->move == anser){
			return true;
		}
	}
	return false;
}
//ms[512]を直前にゼロクリアすることを利用してどれくらい
//データを積み上げたかカウントアップする。
int array_count(MoveStack *m)
{
	int count = 0;
	for (int i = 0; i < 512; i++){
		if (m++->move == 0){
			return count;
		}
		count++;
	}
	return count;
}
